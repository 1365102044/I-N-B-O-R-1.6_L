#import "YXHomePageBadRequestView.h"

@implementation YXHomePageBadRequestView

/**
 重新请求网络

 @param sender 重新请求网络按钮
 */
- (IBAction)reRequestButtonClick:(id)sender
{
    if ([self.delegate respondsToSelector:@selector(homePageBadRequestView:)]) {
        [self.delegate homePageBadRequestView:self];
    }
}


@end
